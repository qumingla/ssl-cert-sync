type TranslateParams = Record<string, string | number>;

interface LocaleContextType {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: TranslationKey, params?: TranslateParams) => string;
  formatRelative: (value: Date | string | number | null | undefined) => string;
  statusLabel: (status: string) => string;
  jobTypeLabel: (type: string) => string;
  eventTypeLabel: (type: string) => string;
}

const LocaleContext = createContext<LocaleContextType | null>(null);

function getInitialLanguage(): Language {
  const stored = localStorage.getItem(STORAGE_KEY);
  if (stored === "zh-CN" || stored === "en-US") {
    return stored;
  }
  return "zh-CN";
}

function interpolate(template: string, params?: TranslateParams) {
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? ""));
}

export function LocaleProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>(getInitialLanguage);

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language;
  }, [language]);

  const setLanguage = useCallback((nextLanguage: Language) => {
    setLanguageState(nextLanguage);
  }, []);

  const t = useCallback((key: TranslationKey, params?: TranslateParams) => {
    return interpolate(translations[language][key] ?? translations["en-US"][key] ?? key, params);
  }, [language]);

  const formatRelative = useCallback((value: Date | string | number | null | undefined) => {
    if (!value) {
      return translations[language]["common.never"];
    }

    const options = language === "zh-CN" ? { addSuffix: true, locale: zhCN } : { addSuffix: true };
    return formatDistanceToNow(new Date(value), options);
  }, [language]);

  const statusLabel = useCallback((status: string) => {
    return t(statusKeys[status] ?? "common.unknown");
  }, [t]);

  const jobTypeLabel = useCallback((type: string) => {
    return t(jobTypeKeys[type] ?? "common.unknown");
  }, [t]);

  const eventTypeLabel = useCallback((type: string) => {
    return t(eventTypeKeys[type] ?? "common.unknown");
  }, [t]);

  const value = useMemo(() => ({
    language,
    setLanguage,
    t,
    formatRelative,
    statusLabel,
    jobTypeLabel,
    eventTypeLabel,
  }), [eventTypeLabel, formatRelative, jobTypeLabel, language, setLanguage, statusLabel, t]);

  return (
    <LocaleContext.Provider value={value}>
      {children}
    </LocaleContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(LocaleContext);
  if (!context) {
    throw new Error("useI18n must be used within a LocaleProvider");
  }
  return context;
}
